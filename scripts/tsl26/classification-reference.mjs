export function labelForValue(value, locale = 'en') {
  return CATEGORY_LABELS[value]?.[locale] || MUSCLE_TRANSLATIONS[value]?.[locale] || EQUIPMENT_LABELS[value]?.[locale] || String(value || '').replace(/_/g, ' ');
}

export function labelsForValues(values, locale = 'en') {
  return (values || []).map((value) => labelForValue(value, locale));
}
